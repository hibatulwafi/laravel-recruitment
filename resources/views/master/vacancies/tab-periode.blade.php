<script>
  $(document).ready(function() {
    $('.edit-periode-btn').click(function() {
      var primaryId = $(this).data('id');
      $.ajax({
        url: "{{ url('periode/edit')}}/" + primaryId,
        type: 'GET',
        data: {
          id: primaryId
        },
        success: function(data) {
          $('#id_edit').val(data.batch_id);
          var selectElement = document.getElementById('vacancy_id_edit');
          selectElement.value = data.vacancy_id;
          $('#start_date_edit').val(data.start_date);
          $('#end_date_edit').val(data.end_date);
          $('#editPeriodeModal').modal('show');
        },
        error: function() {
          alert('Terjadi kesalahan saat memuat data.');
        }
      });
    });

    $('.delete-periode-btn').click(function() {
      var primaryId = $(this).data('id');
      $.ajax({
        url: "{{ url('periode/detail')}}/" + primaryId,
        type: 'GET',
        data: {
          id: primaryId
        },
        success: function(data) {
          $('#position').html(data.position);
          $('#tanggal').html("{{ \Carbon\Carbon::parse($row->start_date)->format('d M Y') . ' - ' . \Carbon\Carbon::parse($row->end_date)->format('d M Y') }}");
          $('#id_delete').val(data.batch_id);
          $('#deletePeriodeModal').modal('show');
        },
        error: function() {
          alert('Terjadi kesalahan saat memuat data.');
        }
      });
    });
  });
</script>